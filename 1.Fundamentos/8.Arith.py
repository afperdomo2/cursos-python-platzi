# Opradores aritméticos

print(10 + 10)  # 20
print(5 - 2)  # 3
print(8 / 2)  # 4.0
print(8 // 2)  # 4 => // Solo captura el número entero de la división
print(7 * 3)  # 21
print(10 % 2)  # 0
print(2 ** 3)  # 8 => ** Elevar un valor
print(2 ** 3 + 3 - 7 / 1 // 4)  # 10.0

print("hola" + "mundo")  # Concatenar 2 cadenas
print("🍷Salud" * 3)  # Multiplicar un string
